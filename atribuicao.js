/*Armazenando valores com o operador de atribuição
Em JavaScript, você pode armazenar um valor em uma variável com o operador de atribuição= ( ).

myVariable = 5;
Isso atribui o Numbervalor 5a myVariable.

Se houver cálculos à direita do =operador, eles serão executados antes que o valor seja atribuído à variável à esquerda do operador.

var myVar;
myVar = 5;
Primeiro, esse código cria uma variável chamada myVar. Então, o código atribui 5a myVar. Agora, se myVaraparecer novamente no código, o programa tratará como se fosse 5.

Atribuir o valor 7à variável a. */

var a;
