/* Compreendendo a diferenciação entre maiúsculas e minúsculas em variáveis
Em JavaScript, todas as variáveis ​​e nomes de funções são sensíveis a maiúsculas e minúsculas. Isso significa que a capitalização importa.

MYVARnão é o mesmo que MyVarnor myvar. É possível ter várias variáveis ​​distintas com o mesmo nome, mas com maiúsculas e minúsculas diferentes. É altamente recomendável que, por uma questão de clareza, você não use esse recurso de linguagem.

Melhores práticas

Escreva nomes de variáveis ​​em JavaScript em camelCase . Em camelCase , nomes de variáveis ​​multipalavra têm a primeira palavra em minúscula e a primeira letra de cada palavra subsequente é maiúscula.

Exemplos:

var someVariable;
var anotherVariableName;
var thisVariableNameIsSoLong;
Modifique as declarações e atribuições existentes para que seus nomes usem camelCase .

Não crie nenhuma variável nova.*/


