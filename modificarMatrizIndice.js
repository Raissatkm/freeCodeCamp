/* Modificar dados de matriz com índices
Ao contrário das strings, as entradas de arrays são mutáveis ​​e podem ser alteradas livremente, mesmo que o array tenha sido declarado com const.

Exemplo

const ourArray = [50, 40, 30];
ourArray[0] = 15;
ourArrayagora tem o valor [15, 40, 30].

Nota: Não deve haver espaços entre o nome do array e os colchetes, como array [0]. Embora o JavaScript consiga processar isso corretamente, isso pode confundir outros programadores que leem seu código.*/


